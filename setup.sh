if [ "$#" -ne 1 ]; then
    display_help
    exit 1
fi

# Process the provided parameter
case "$1" in
--install)
    display_eula
    check_commands
    check_prereq
    install_k3s
    install_app
    ;;
--remove)
    uninstall
    ;;
--help)
    display_help
    ;;
*)
    echo "Invalid parameter. Use --help to see the available options."
    display_help
    exit 1
    ;;
esac
